public class SearchHotWordEntity {
    private String id;
    private String searchWord;
    private String searchWordNo;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSearchWord() {
        return searchWord;
    }

    public void setSearchWord(String searchWord) {
        this.searchWord = searchWord;
    }

    public String getSearchWordNo() {
        return searchWordNo;
    }

    public void setSearchWordNo(String searchWordNo) {
        this.searchWordNo = searchWordNo;
    }
}
